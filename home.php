<h2 class="art-postheader">Beranda Sistem Pakar Diagnosa Penyakit Jantung</h2>
<div class="art-postcontent">
<p><strong>Sistem Pakar Diagnosa penyakit jantung, merupakan sistem pakar untuk membantu memberikan Informasi dini mengenali gejala-gejala penyakit pada jantung</strong></p>
<p style="font-family:'Verdana', Courier, monospace; color:#336; text-align:justify"><img src="images/4.jpg" width="350" height="280" style="float:right; " /> Penyakit jantung atau dalam istilah medis disebut penyakit jantung koroner adalah kondisi yang terjadi ketika pembuluh darah utama yang menyuplai darah ke jantung (pembuluh darah koroner) mengalami kerusakan. Tumpukan kolesterol pada pembuluh darah serta proses peradangan diduga menjadi penyebab penyakit ini.
Ketika terjadi penumpukan kolesterol (plak), pembuluh darah koroner akan menyempit sehingga aliran darah dan suplai oksigen menuju jantung pun akan terhambat. Kurangnya aliran darah ini akan menyebabkan rasa nyeri pada dada (angina) dan sesak napas, hingga suatu saat terjadi hambatan total pada aliran darah menuju jantung atau yang disebut juga dengan serangan jantung.<br />
</br>
Penyakit jantung koroner termasuk salah satu penyebab kematian tertinggi di dunia. Federasi Jantung Dunia memperkirakan angka kematian akibat penyakit jantung koroner di Asia Tenggara mencapai 1,8 juta kasus pada tahun 2014. Di Indonesia sendiri pada tahun 2013 tercatat ada setidaknya 883.447 orang yang terdiagnosis penyakit jantung koroner di Indonesia dengan mayoritas penderita berusia 55-64 tahun. Angka kematian akibat penyakit jantung pun cukup tinggi, yakni sekitar 45 persen dari seluruh angka kematian di Indonesia.
</p>
<p style="font-family:Verdana, Geneva, sans-serif; text-align:justify"><img src="images/6.jpg" width="350" height="230" style="float:left;" />Menjaga kesehatan jantung itu mudah, salah satunya adalah dengan pola makan yang sehat. Namun kadang manusia banyak khilafnya jadi makanan yang terlihat enak masuk perut tanpa memikirkan efeknya bagi kesehatan. Rajin-rajinlah mengonsumsi sayur hijau, kacang-kacangan, ikan mengandung omega-3. Serta membatasi konsumsi junkfood, gula, lemak, dan garam berlebihan.
</br>
</br>

Kemudian rutin berolah raga setiap hari, Untuk menjaga kebugaran tubuh dan kardiovaskular, pastikan kamu berolahraga setiap hari. Lakukan beberapa aktivitas fisik minimal 30 menit setiap hari. Jangan bilang anda anda tidak punya waktu untuk melakukannya.
</p>
<p style="font-family:'Verdana', Courier, monospace; color:#336; text-align:justify">
</br>
</br>
Pencegahan Penyakit Jantung Koroner
Terdapat beberapa langkah pencegahan yang sederhana untuk menghindari penyakit jantung, yaitu:</br></br>
1. Berhenti merokok.</br>
2. Menerapkan pola hidup sehat, misalnya mengurangi makanan 
   berkolesterol tinggi,memperbanyak konsumsi &nbsp;&nbsp;&nbsp;&nbsp;buah-buahan dan sayuran, serta berolahraga secara teratur.</br>
3. Mengontrol kadar gula darah.</br>
4. Menjaga berat badan yang sehat.</br>
5. Mengurangi konsumsi minuman keras.
</p>
